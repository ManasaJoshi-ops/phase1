package one;

public class switchcase {
	public static void main(String[] args) {
		int UserOptions=1;
		
		switch(UserOptions) {
		case 1:System.out.println("Main Menu");
		break;
		case 2:System.out.println("delete option");
		break;
		case 3:System.out.println("Back");
		break;
		case 4:System.out.println("Next page");
		break;
		default:System.out.println("close screen");
		
		
		}
	}

}
